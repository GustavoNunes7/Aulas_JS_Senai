// `var` →  Antiga, pode ser redeclarado e mudar de valor.
 var cor = "azul";
 var cor = "vermelho";
  console.log(cor); 


// `let` → Mais moderno, pode mudar de valor, mas não pode ser redeclarado

let fruta = "banana";
fruta = "maça";
console.log(fruta);


// `const`  → Fixo, não pode mudar de valor
const cpf = 1234567890;
console.log(cpf);