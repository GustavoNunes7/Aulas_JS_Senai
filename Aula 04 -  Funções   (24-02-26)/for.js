// FOR
//O For é utilizado quando é necessário repetir um trecho de um código várias vezes, geralmente sabendo ***QUANTAS VEZES *** ele deve se repetir

// //Estrutura
// for(início, condição, incremento){
//     //código que será repetido
// }
//início ---> Onde começa
//condição ---> Até quando repete
//incrimento --> Como o valor muda a cada volta


// Exemplo
for(let i=1; i <= 5; i++){
    console.log(i)
} //i++ é adicionar um a mais